    /**
     * 初始化各种类加载器。
     */
    private void initClassLoaders() {
        try {
            //置空  目的：打破双亲委派模型机制。因为浪费性能。
            commonLoader = createClassLoader("common", null);
            if (commonLoader == null) {
                // no config file, default to this loader - we might be in a 'single' env.
                /**
                 * Connected to the target VM, address:127.0.0.1:1083 tranpsort:'socket'
                 * 从Console中的日志可以看出。网络通讯有关。
                 * Bootstrap.getClassLoader = URLClassLoader.
                 */
                commonLoader = this.getClass().getClassLoader();
            }
            log.info("org.apache.catalina.startup.Bootstrap.initClassLoaders commonLoader 类型为:"+ commonLoader.getClass().getName());
            //指向了commonLoader
            catalinaLoader = createClassLoader("server", commonLoader);//(打一个断点观察catalinaLoader的值)
            log.info("org.apache.catalina.startup.Bootstrap.initClassLoaders catalinaLoader 类型为:"+ catalinaLoader.getClass().getName());
            //指向了commonLoader
            sharedLoader = createClassLoader("shared", commonLoader);//(打断点观测sharedLoader的值。)
            log.info("org.apache.catalina.startup.Bootstrap.initClassLoaders commonLoader 类型为:"+ commonLoader.getClass().getName());

        } catch (Throwable t) {
            handleThrowable(t);
            log.error("Class loader creation threw exception", t);
            System.exit(1);
        }
    }

    /**
     * 创建类加载器
     * @param name
     * @param parent
     * @return
     * @throws Exception
     */
    private ClassLoader createClassLoader(String name, ClassLoader parent)
            throws Exception {

        String value = CatalinaProperties.getProperty(name + ".loader");
        if ((value == null) || (value.equals("")))
            return parent;

        value = replace(value);

        List<Repository> repositories = new ArrayList<>();

        String[] repositoryPaths = getPaths(value);

        for (String repository : repositoryPaths) {
            // Check for a JAR URL repository
            try {
                @SuppressWarnings("unused")
                URL url = new URL(repository);
                repositories.add(new Repository(repository, RepositoryType.URL));
                continue;
            } catch (MalformedURLException e) {
                // Ignore
            }

            // Local repository
            if (repository.endsWith("*.jar")) {
                repository = repository.substring
                        (0, repository.length() - "*.jar".length());
                repositories.add(new Repository(repository, RepositoryType.GLOB));
            } else if (repository.endsWith(".jar")) {
                repositories.add(new Repository(repository, RepositoryType.JAR));
            } else {
                repositories.add(new Repository(repository, RepositoryType.DIR));
            }
        }

        return ClassLoaderFactory.createClassLoader(repositories, parent);
    }


    /**
     * System property replacement in the given string.
     *
     * @param str The original string
     * @return the modified string
     */
    protected String replace(String str) {
        // Implementation is copied from ClassLoaderLogManager.replace(),
        // but added special processing for catalina.home and catalina.base.
        String result = str;
        int pos_start = str.indexOf("${");
        if (pos_start >= 0) {
            StringBuilder builder = new StringBuilder();
            int pos_end = -1;
            while (pos_start >= 0) {
                builder.append(str, pos_end + 1, pos_start);
                pos_end = str.indexOf('}', pos_start + 2);
                if (pos_end < 0) {
                    pos_end = pos_start - 1;
                    break;
                }
                String propName = str.substring(pos_start + 2, pos_end);
                String replacement;
                if (propName.length() == 0) {
                    replacement = null;
                } else if (Globals.CATALINA_HOME_PROP.equals(propName)) {
                    replacement = getCatalinaHome();
                } else if (Globals.CATALINA_BASE_PROP.equals(propName)) {
                    replacement = getCatalinaBase();
                } else {
                    replacement = System.getProperty(propName);
                }
                if (replacement != null) {
                    builder.append(replacement);
                } else {
                    builder.append(str, pos_start, pos_end + 1);
                }
                pos_start = str.indexOf("${", pos_end + 1);
            }
            builder.append(str, pos_end + 1, str.length());
            result = builder.toString();
        }
        return result;
    }


    /**
     * Initialize daemon.
     * @throws Exception Fatal initialization error
     *
     * 1. 初始化类加载器。
     * 2. 实例化Catalina对象。 daemon的设置。
     * 3. Catalina去init Server对象。
     */
    public void init() throws Exception {
        //初始化类加载器。
        initClassLoaders();//(断点在这儿打一个。)
        Thread.currentThread().setContextClassLoader(catalinaLoader);

        SecurityClassLoad.securityClassLoad(catalinaLoader);

        // Load our startup class and call its process() method
        if (log.isDebugEnabled())
            log.debug("Loading startup class");
        //反射去加载Catalina类并进行实例化。
        Class<?> startupClass = catalinaLoader.loadClass("org.apache.catalina.startup.Catalina");
        Object startupInstance = startupClass.getConstructor().newInstance();

        // Set the shared extensions class loader
        if (log.isDebugEnabled())
            log.debug("Setting startup class properties");
        String methodName = "setParentClassLoader";
        Class<?> paramTypes[] = new Class[1];
        paramTypes[0] = Class.forName("java.lang.ClassLoader");
        Object paramValues[] = new Object[1];
        paramValues[0] = sharedLoader;
        /**
         * 反射执行对应setParentClassLoader方法。
         * 详见{@link Catalina#setParentClassLoader(ClassLoader)}
         * (打断点观测下获取的方法的名称和参数 以及真正执行的方法)
         */
        Method method =
                startupInstance.getClass().getMethod(methodName, paramTypes);
        method.invoke(startupInstance, paramValues);
        //将Catalina设置会守护进程。
        catalinaDaemon = startupInstance;
    }


    /**
     * Load daemon.
     * load的时候 这时候有参数了。
     */
    private void load(String[] arguments) throws Exception {

        // Call the load() method
        String methodName = "load";
        Object param[];
        Class<?> paramTypes[];
        if (arguments==null || arguments.length==0) {
            paramTypes = null;
            param = null;
        } else {
            paramTypes = new Class[1];
            paramTypes[0] = arguments.getClass();
            param = new Object[1];
            param[0] = arguments;
        }
        Method method =
                catalinaDaemon.getClass().getMethod(methodName, paramTypes);
        if (log.isDebugEnabled()) {
            log.debug("Calling startup class " + method);
        }
        //反射调用Catalina。
        method.invoke(catalinaDaemon, param);
    }


