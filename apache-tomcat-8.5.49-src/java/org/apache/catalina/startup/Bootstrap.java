    /**

     * 1.静态代码块儿在类中的加载顺序。详见深入理解JVM。
     *  1.1 获取bin目录的绝对路径。
     *  1.2.设置安装目录的路径。
     *  1.3.设置工作目录的路径。
     * 也就是启动参数中的参数设置：
     *  -Dcatalina.home=catalina-home   //安装目录。
     *  -Dcatalina.base=catalina-home   //工作目录。
     *  从环境变量找,找不到后就去设置即可。
     *
     */
    static {
        log.info("Bootstrap static {} start!");
        // Will always be non-null
        /**
         * C:\Users\chenc\Downloads\MySelf\Tomcat8-Source-Read ($CATALINA_HOME\bin：用户目录的绝对路径。)
         */
        String userDir = System.getProperty("user.dir");

        // Home first
        /**
         * catalina-home (Tomcat的根目录：当前项目的相对路径)
         */
        String home = System.getProperty(Globals.CATALINA_HOME_PROP);
        File homeFile = null;

        //获取的是tomcat的安装目录.即：磁盘下catalina-home下绝对路径下的所有的文件夹和文件封装成File。
        if (home != null) {
            File f = new File(home);
            try {
                homeFile = f.getCanonicalFile();
            } catch (IOException ioe) {
                homeFile = f.getAbsoluteFile();
            }
        }

        if (homeFile == null) {
            // First fall-back. See if current directory is a bin directory
            // in a normal Tomcat install
            //正常安装下查看是不是bin目录。
            File bootstrapJar = new File(userDir, "bootstrap.jar");

            if (bootstrapJar.exists()) {
                File f = new File(userDir, "..");
                try {
                    homeFile = f.getCanonicalFile();
                } catch (IOException ioe) {
                    homeFile = f.getAbsoluteFile();
                }
            }
        }

        if (homeFile == null) {
            // Second fall-back. Use current directory
            File f = new File(userDir);
            try {
                homeFile = f.getCanonicalFile();
            } catch (IOException ioe) {
                homeFile = f.getAbsoluteFile();
            }
        }
        //工作目录和安装目录默认是一致的。可以设为不一致。
        //环境变量设置(安装目录)。 设置catalinaHomeFile = 当前bin文件夹绝对路径。
        catalinaHomeFile = homeFile;
        System.setProperty(
                Globals.CATALINA_HOME_PROP, catalinaHomeFile.getPath());

        // Then base
        String base = System.getProperty(Globals.CATALINA_BASE_PROP);
        if (base == null) { //设置工作目录。
            catalinaBaseFile = catalinaHomeFile;
        } else {
            File baseFile = new File(base);
            try {
                baseFile = baseFile.getCanonicalFile();
            } catch (IOException ioe) {
                baseFile = baseFile.getAbsoluteFile();
            }
            catalinaBaseFile = baseFile;
        }
        System.setProperty(
                Globals.CATALINA_BASE_PROP, catalinaBaseFile.getPath());
        log.info("Bootstrap static {} end!");
    }

